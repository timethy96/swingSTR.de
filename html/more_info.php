<h2>swingSTR | Der offene Swing-Event-Kalender für Stuttgart und Umgebung</h2>
<p>
    In diesem offenen Kalender kann jede*r die Events eintragen, die er*sie veranstaltet, oder nur von denen er*sie gehört hat.<br/>
    So sollen Swing-Events, ob es Tanzparties, Swing-Konzerte, Kurse etc. sind, für alle verfügbar gemacht werden.<br/>
    <br/>
    Wenn du ein Event hinzugefügt hast, muss es noch von einem Moderator geprüft werden. Daher kann es sein, dass dein Event nicht sofort im Kalender auftaucht.<br/>
    <br/>
    Der Kalender intergriert auch die bestehenden Kalender verschiedener Tanzschulen in Stuttgart.<br/>
